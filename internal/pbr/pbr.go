type gen struct {
	engine *engine
	pkg    *packages.Package
	routes []*RoutePackage
}

func newGen(engine *engine, pkg *packages.Package, routes []*RoutePackage) *gen {
	return &gen{
		engine: engine,
		pkg:    pkg,
		routes: routes,
	}
}

func Load() error {
	wd, err := os.Getwd()
	if err != nil {
		fmt.Println("failed to get working directory: ", err)
		return err
	}
	fmt.Println(wd)

	cfg := &packages.Config{
		// Context:    ctx,
		Mode: packages.NeedName | packages.NeedFiles | packages.NeedCompiledGoFiles | // LoadFiles
			packages.NeedImports | // LoadImports
			packages.NeedTypes | packages.NeedTypesSizes | // LoadTypes
			packages.NeedSyntax | packages.NeedTypesInfo | // LoadSyntax
			packages.NeedDeps, // LoadTypes
		Dir: wd,
		// Env:        env,
		BuildFlags: []string{"-tags=pbrinject"},
		// TODO(light): Use ParseFile to skip function bodies and comments in indirect packages.
	}

	pkgs, err := packages.Load(cfg)
	if err != nil {
		return err
	}

	// TODO: lazy load
	e := defaultEngine()
	r := newRouteGen(e.TargetSels(), e.MiddlewareSelector())
	routes := r.parseRoute(wd)

	for _, pkg := range pkgs {
		fmt.Println("path", pkg.PkgPath)

		for _, f := range pkg.Syntax {
			buildFuncs := make(map[*ast.FuncDecl]bool)

			ast.Inspect(f, func(n ast.Node) bool {
				fn, ok := n.(*ast.FuncDecl)
				if !ok {
					return true
				}

				if buildCall, err := findInjectorBuild(pkg.TypesInfo, fn); err != nil {
					fmt.Println("findInjectorBuild error", err.Error())
					return true
				} else if buildCall != nil {
					// inject build found
					buildFuncs[fn] = true
				}
				return true
			})

			if len(buildFuncs) > 0 {
				g := newGen(e, pkg, routes)
				g.inject(f, buildFuncs)
			}
		}
	}

	return nil
}

func (g *gen) generate(f *ast.File) error {
	oriFilePath := g.pkg.Fset.File(f.Pos()).Name()

	filename := filepath.Base(oriFilePath)
	ext := strings.ToLower(filepath.Ext(filename))
	nonExt := filename[:len(filename)-len(ext)]
	dir := filepath.Dir(oriFilePath)
	outputPath := dir + "/" + nonExt + "_gen" + ext

	out, err := os.Create(outputPath)
	if err != nil {
		return err
	}
	defer out.Close()

	out.WriteString("// Code generated by pbr. DO NOT EDIT.\n")
	out.WriteString("\n")
	out.WriteString("//go:build !pbrinject\n")
	out.WriteString("// +build !pbrinject\n")
	out.WriteString("\n")
	out.WriteString("package " + g.pkg.Name + "\n")
	out.WriteString("\n")

	for i, decl := range f.Decls {
		printAST(token.NewFileSet(), decl)
		fmt.Print("\n")

		if err := printer.Fprint(out, token.NewFileSet(), decl); err != nil {
			return err
		}

		if i < len(f.Decls)-1 {
			out.WriteString("\n\n")
		} else {
			out.WriteString("\n") // EOF
		}
	}

	return nil
}

func (g *gen) inject(f *ast.File, buildFunc map[*ast.FuncDecl]bool) {
	// build imports
	scopes := make([]*types.Scope, len(buildFunc))
	for fn := range buildFunc {
		scopes = append(scopes, g.pkg.TypesInfo.Scopes[fn.Type])
	}
	n := newNamerWithScopes(scopes)
	specs, routePackagesImports := g.importsFromRoutes(g.routes, n)

	astutil.Apply(f, func(c *astutil.Cursor) bool {
		if imp, ok := c.Node().(*ast.ImportSpec); ok {
			// inject imports
			if imp.Path.Value == `"github.com/serkodev/pbr"` {
				for _, spec := range specs {
					c.InsertBefore(spec)
				}
				c.Delete()
			}
		} else if fn, ok := c.Node().(*ast.FuncDecl); ok {
			// inject body
			if buildFunc[fn] {
				// TODO: load config first, then replace the fn.Body
				astutil.Apply(fn.Body, func(c *astutil.Cursor) bool {
					if stmt, ok := c.Node().(ast.Stmt); ok {
						if s := getInjectorStmt(g.pkg.TypesInfo, stmt); s != nil {
							for _, arg := range s.Args {
								obj := qualifiedIdentObject(g.pkg.TypesInfo, arg)
								if obj != nil && g.engine.ValidInjectType(obj.Type()) {
									ident := arg.(*ast.Ident)
									scope := g.pkg.TypesInfo.Scopes[fn.Type]
									n := newNamer(scope)
									for _, stmt := range g.buildInjectStmts(ident, g.routes, routePackagesImports, n) {
										c.InsertBefore(stmt)
									}
									c.Delete()
									return true
								}
							}
						}
					}
					return true
				}, nil)
			}
		}
		return true
	}, nil)

	g.generate(f)
}

func (g *gen) importsFromRoutes(routes []*RoutePackage, n *namer) ([]*ast.ImportSpec, map[*RoutePackage]*ast.ImportSpec) {
	var specs []*ast.ImportSpec
	routePackagesImports := make(map[*RoutePackage]*ast.ImportSpec)
	for _, r := range routes {
		if r.RelativePath != "." {
			newName := n.gen("pbr_route")
			spec := &ast.ImportSpec{
				Path: &ast.BasicLit{
					Kind:  token.STRING,
					Value: `"` + r.PkgPath + `"`,
				},
				Name: ast.NewIdent(newName),
			}
			routePackagesImports[r] = spec
			specs = append(specs, spec)
		}
		// sub packages
		if len(r.SubPackages) > 0 {
			subSpecs, subImports := g.importsFromRoutes(r.SubPackages, n)
			specs = append(specs, subSpecs...)
			for k, v := range subImports {
				routePackagesImports[k] = v
			}
		}
	}
	return specs, routePackagesImports
}

func (g *gen) buildInjectStmts(ident *ast.Ident, routePkgs []*RoutePackage, routePackagesImports map[*RoutePackage]*ast.ImportSpec, n *namer) []ast.Stmt {
	var stmts []ast.Stmt

	for _, routePkg := range routePkgs {
		routePkgIdent := ident
		routePkgPath := routePkg.routePath()

		imp := ""
		if routeImport, ok := routePackagesImports[routePkg]; ok {
			imp = routeImport.Name.Name
		}

		for _, r := range routePkg.Routes {
			rIdent := routePkgIdent
			rPath := buildRoutePath(routePkgPath, r.Path)

			if r.hasMiddleware() && g.engine.Middleware != nil {
				var groupIdent = ast.NewIdent(n.gen("grp"))
				var groupRoutePath = rPath

				if r.isRootRoute() {
					groupRoutePath = routePkgPath

					// update routePkg
					routePkgIdent = groupIdent
					routePkgPath = ""
				}

				// generate expr with template
				expr := g.engine.GenGroup(rIdent, groupRoutePath)
				stmts = append(stmts, mustParseExprF(`%s := %s`, groupIdent.Name, expr))

				rIdent = groupIdent
				rPath = ""
			}

			// sub
			callObj := imp
			if !r.isRootRoute() {
				typeVar := n.gen(strings.ToLower(r.Name))
				sub := r.Name
				if imp != "" {
					sub = imp + "." + sub
				}
				stmts = append(stmts, mustParseExprF(`%s := &%s{}`, typeVar, sub))
				callObj = typeVar
			}

			if rstmts := g.buildInjectSels(rIdent, r.Sels, callObj, rPath); len(rstmts) > 0 {
				stmts = append(stmts, rstmts...)
			}
		}

		// sub packages
		if len(routePkg.SubPackages) > 0 {
			stmts = append(stmts, g.buildInjectStmts(routePkgIdent, routePkg.SubPackages, routePackagesImports, n)...)
		}
	}

	return stmts
}

func (g *gen) buildInjectSels(ident *ast.Ident, sels []string, callObj string, routePath string) []ast.Stmt {
	var stmts []ast.Stmt

	for _, sel := range sels {
		var handle = sel
		if callObj != "" {
			handle = callObj + "." + handle
		}

		expr := g.engine.GenSel(ident, sel, routePath, handle)
		if expr != "" {
			stmts = append(stmts, mustParseExpr(expr))
		}
	}

	return stmts
}
